const questionElement = document.getElementById("question");
const answerButton = document.getElementById("answerbuttons");
const nextButton = document.getElementById("nextBtn");
const queenYasmin = document.getElementById("queenyasmin");
const startButton = document.getElementById("startButton");

let currentQuestionIndex = 0;
let score = 0;


function startQuiz(){
    currentQuestionIndex = 0;
    score = 0;
    nextButton.innerHTML = "Next";
    showQuestion();
}

function showQuestion(){
    resetState();
    
    let currentQuestion = questions[currentQuestionIndex] //refers to the questions array initialized at the start, with questions and answers
    let questionNo = currentQuestionIndex + 1; //the number beside the question in the page
    questionElement.innerHTML = questionNo + ". " + currentQuestion.question; //takes the question of the first array in questions[]

    //answer reveal after pressing the button
    currentQuestion.answers.forEach(answer => {
        const button = document.createElement("button");
        button.innerHTML = answer.text;
        button.classList.add("btnChoice");
        answerButton.appendChild(button); //Display on the html button element
        if(answer.correct){
            button.dataset.correct = answer.correct;
        }
        button.addEventListener("click", selectAnswer);
        //selectAnswer function displays if the clicked answer is right or wrong
    })
}

function resetState(){
    nextButton.style.display = "none";
    queenYasmin.style.display = "none";
    while(answerButton.firstChild){
        answerButton.removeChild(answerButton.firstChild);
    }
}

//This function creates a new class list to the buttons, so we can display green in the answer is correct or red if it is wrong
function selectAnswer(e){
    const selectedBtn = e.target; //The button you clicked will be sent to selectedBtn variable to check if it's right or wrong depending on the boolean value
    const isCorrect = selectedBtn.dataset.correct === "true";
    if(isCorrect){
        selectedBtn.classList.add("correct");
        score ++;
    }else{
        selectedBtn.classList.add("incorrect");
    }
    //Shows the correct answers in the event that the user chooses the wrong answer
    Array.from(answerButton.children).forEach(button => {
        if(button.dataset.correct === "true"){
            button.classList.add("correct");
        }
        button.disabled = true;
    });
    nextButton.style.display = "block";
}

function showScore(){
    resetState();
    questionElement.innerHTML = `You scored ${score} out of ${questions.length}! SENd sum luv to the creator BAHAHAHAHAHHA TY!! <3`;
    nextButton.innerHTML = "Play Again Bhie";
    nextButton.style.display = "block";
    queenYasmin.style.display = "block";
}

function handleNextButton(){
    currentQuestionIndex++;
    if(currentQuestionIndex < questions.length){
        showQuestion();
    }else{
        showScore();
    }
}

nextButton.addEventListener("click", ()=>{
    if(currentQuestionIndex < questions.length){
        handleNextButton();
    }else{
        startQuiz();
    }
})
startQuiz();
